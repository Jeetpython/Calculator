operation = input("Which operation, +, -, * or /? ")
number1  = int(input("Number One?"))
number2 = int(input("Number two?"))

if operation == "+" :
    print(number1 + number2)
    
if operation == "-" :
    print(number1 - number2)

if operation == "*" :
    print(number1 * number2)

if operation == "/" :
    print(number1 / number2)
